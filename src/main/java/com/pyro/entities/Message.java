package com.pyro.entities;

import javax.persistence.*;

@Entity
public class Message extends AbstractEntity {

    @Column
    private String text;

    public Message() {
    }

    public Message(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

}
